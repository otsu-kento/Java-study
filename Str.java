public class Str {
    public static void main(String[] args) {
        // char 文字1つ シングルコーテーションでくくる
        char var01 = 'c';
        // String 文字2つ以上 ダブルコーテーションでくくる 厳密にはStringというクラス
        String var02 = "hello, World!";

        System.out.println(var01);
        System.out.println(var02);
    }
}
